  contentManagerForm: FormGroup;

  constructor(private _formBuilder: FormBuilder) {
  }

  ngOnInit(): void {
    //https://stackblitz.com/edit/angular-3eba9k?file=src%2Fapp%2Fapp.component.html,src%2Fapp%2Fapp.component.ts - nested reactive forms
    this.contentManagerForm = this._formBuilder.group({
      componentControl: this._formBuilder.array([this.addControlsToComponentGroup()])
    });
  }

  private addControlsToComponentGroup(): FormGroup {
    return this._formBuilder.group({
      textAreaControl: [],
      chipControl: []
    });
  }

  addControlsToComponent(): void {
    this.componentControlArray.push(this.addControlsToComponentGroup());
  }

  // @ts-ignore
  get componentControlArray(): FormArray {
    return <FormArray>this.contentManagerForm.get('componentControl');
  }

  removeAddress(index: number): void {
    this.componentControlArray.removeAt(index);
  }



  //https://stackblitz.com/edit/angular-aabsb6?file=src%2Fapp%2Fapp.component.html,src%2Fapp%2Fapp.component.ts
  onReset() {
    // reset whole form back to initial state
    this.contentManagerForm.reset();
  }


